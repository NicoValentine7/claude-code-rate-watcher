use crate::api_client::ApiRateLimitData;
use serde::Deserialize;
use std::path::PathBuf;
use std::time::{Duration, SystemTime};

const RATE_DATA_FILENAME: &str = "ccrw-rate-data.json";
const HELPER_SCRIPT_FILENAME: &str = "ccrw-statusline.sh";
const STALE_THRESHOLD: Duration = Duration::from_secs(600); // 10 minutes

#[derive(Deserialize)]
struct StatusLineRateData {
    five_hour_percent: Option<u32>,
    seven_day_percent: Option<u32>,
    five_hour_resets_at: Option<String>,
    seven_day_resets_at: Option<String>,
    timestamp: Option<u64>,
}

/// Path to the rate data file written by the statusline helper script.
pub fn rate_data_path() -> Option<PathBuf> {
    dirs::home_dir().map(|h| h.join(".claude").join(RATE_DATA_FILENAME))
}

/// Path to the helper script.
fn helper_script_path() -> Option<PathBuf> {
    dirs::home_dir().map(|h| h.join(".claude").join(HELPER_SCRIPT_FILENAME))
}

/// Read and parse the statusline rate data file.
/// Returns None if the file doesn't exist, can't be parsed, or is stale.
pub fn read_rate_data() -> Option<ApiRateLimitData> {
    let path = rate_data_path()?;
    let content = std::fs::read_to_string(&path).ok()?;
    let data: StatusLineRateData = serde_json::from_str(&content).ok()?;

    // Check staleness via embedded timestamp
    if let Some(ts) = data.timestamp {
        let now = SystemTime::now()
            .duration_since(SystemTime::UNIX_EPOCH)
            .unwrap_or_default()
            .as_secs();
        if now.saturating_sub(ts) > STALE_THRESHOLD.as_secs() {
            eprintln!("[statusline] Data is stale ({}s old), falling back to API", now - ts);
            return None;
        }
    } else {
        // No timestamp — check file mtime
        if !is_file_fresh(&path) {
            return None;
        }
    }

    // Convert resets_at from unix epoch seconds to RFC3339 if it's a pure number
    let convert_resets_at = |s: &Option<String>| -> Option<String> {
        s.as_ref().and_then(|v| {
            if let Ok(epoch) = v.parse::<i64>() {
                let dt = chrono::DateTime::from_timestamp(epoch, 0)?;
                Some(dt.to_rfc3339())
            } else {
                // Already a string (possibly RFC3339), pass through
                Some(v.clone())
            }
        })
    };

    Some(ApiRateLimitData {
        five_hour_percent: data.five_hour_percent,
        seven_day_percent: data.seven_day_percent,
        five_hour_resets_at: convert_resets_at(&data.five_hour_resets_at),
        seven_day_resets_at: convert_resets_at(&data.seven_day_resets_at),
        is_live: true,
        auth_missing: false,
        error_message: None,
        error_detail: None,
        retry_count: 0,
        retry_at: None,
        suggest_relogin: false,
    })
}

/// Check if the file was modified within the stale threshold.
fn is_file_fresh(path: &PathBuf) -> bool {
    std::fs::metadata(path)
        .and_then(|m| m.modified())
        .map(|mtime| {
            SystemTime::now()
                .duration_since(mtime)
                .unwrap_or_default()
                < STALE_THRESHOLD
        })
        .unwrap_or(false)
}

/// Install the helper script and configure Claude Code's settings.json.
/// Returns Ok(true) if newly installed, Ok(false) if already configured.
pub fn install() -> Result<bool, String> {
    let script_path = helper_script_path().ok_or("Cannot find home directory")?;
    let data_path = rate_data_path().ok_or("Cannot find home directory")?;

    // Write helper script
    let script_content = format!(
        r#"#!/bin/bash
# CCRW StatusLine Helper — auto-generated by claude-code-rate-watcher
# Reads Claude Code statusline JSON from stdin, extracts rate_limits,
# and writes them to {data_path} for CCRW to consume.

input=$(cat)
echo "$input" | python3 -c "
import sys, json, time
try:
    data = json.load(sys.stdin)
    rl = data.get('rate_limits', {{}})
    if not rl:
        sys.exit(0)
    out = {{}}
    fh = rl.get('five_hour')
    if fh:
        out['five_hour_percent'] = int(fh.get('used_percentage', 0))
        ra = fh.get('resets_at')
        if ra is not None:
            out['five_hour_resets_at'] = str(ra)
    sd = rl.get('seven_day')
    if sd:
        out['seven_day_percent'] = int(sd.get('used_percentage', 0))
        ra = sd.get('resets_at')
        if ra is not None:
            out['seven_day_resets_at'] = str(ra)
    out['is_live'] = True
    out['timestamp'] = int(time.time())
    with open('{data_path}', 'w') as f:
        json.dump(out, f)
except Exception:
    pass
" 2>/dev/null
"#,
        data_path = data_path.display()
    );

    std::fs::write(&script_path, script_content)
        .map_err(|e| format!("Failed to write helper script: {}", e))?;

    // Make executable
    #[cfg(unix)]
    {
        use std::os::unix::fs::PermissionsExt;
        let perms = std::fs::Permissions::from_mode(0o755);
        std::fs::set_permissions(&script_path, perms)
            .map_err(|e| format!("Failed to set script permissions: {}", e))?;
    }

    // Update ~/.claude/settings.json
    configure_settings(&script_path)?;

    eprintln!("[statusline] Helper script installed at {}", script_path.display());
    Ok(true)
}

/// Remove the helper script and restore settings.json.
pub fn uninstall() -> Result<(), String> {
    let script_path = helper_script_path().ok_or("Cannot find home directory")?;
    let data_path = rate_data_path().ok_or("Cannot find home directory")?;

    // Remove script and data files
    let _ = std::fs::remove_file(&script_path);
    let _ = std::fs::remove_file(&data_path);

    // Remove statusLine from settings.json
    unconfigure_settings()?;

    eprintln!("[statusline] Helper script uninstalled");
    Ok(())
}

/// Check if the statusline integration is currently installed.
pub fn is_installed() -> bool {
    let settings_path = match dirs::home_dir() {
        Some(h) => h.join(".claude").join("settings.json"),
        None => return false,
    };
    let script_path = match helper_script_path() {
        Some(p) => p,
        None => return false,
    };

    if !script_path.exists() {
        return false;
    }

    // Check if settings.json points to our script
    if let Ok(content) = std::fs::read_to_string(&settings_path) {
        if let Ok(settings) = serde_json::from_str::<serde_json::Value>(&content) {
            if let Some(cmd) = settings.get("statusLine")
                .and_then(|v| v.get("command"))
                .and_then(|v| v.as_str())
            {
                return cmd.contains(HELPER_SCRIPT_FILENAME);
            }
        }
    }

    false
}

/// Configure the statusLine entry in ~/.claude/settings.json.
fn configure_settings(script_path: &PathBuf) -> Result<(), String> {
    let settings_path = dirs::home_dir()
        .ok_or("Cannot find home directory")?
        .join(".claude")
        .join("settings.json");

    let mut settings: serde_json::Value = if settings_path.exists() {
        let content = std::fs::read_to_string(&settings_path)
            .map_err(|e| format!("Failed to read settings.json: {}", e))?;
        serde_json::from_str(&content)
            .map_err(|e| format!("Failed to parse settings.json: {}", e))?
    } else {
        serde_json::json!({})
    };

    // Save existing statusLine command for chaining (if it's not ours)
    if let Some(existing) = settings.get("statusLine")
        .and_then(|v| v.get("command"))
        .and_then(|v| v.as_str())
    {
        if !existing.contains(HELPER_SCRIPT_FILENAME) {
            eprintln!("[statusline] Existing statusLine command will be overridden: {}", existing);
            // Store the original for potential restoration
            settings["_ccrw_original_statusline"] = serde_json::Value::String(existing.to_string());
        }
    }

    settings["statusLine"] = serde_json::json!({
        "type": "command",
        "command": script_path.to_string_lossy()
    });

    let output = serde_json::to_string_pretty(&settings)
        .map_err(|e| format!("Failed to serialize settings: {}", e))?;
    std::fs::write(&settings_path, output)
        .map_err(|e| format!("Failed to write settings.json: {}", e))?;

    Ok(())
}

/// Remove statusLine config from settings.json, restoring original if saved.
fn unconfigure_settings() -> Result<(), String> {
    let settings_path = dirs::home_dir()
        .ok_or("Cannot find home directory")?
        .join(".claude")
        .join("settings.json");

    if !settings_path.exists() {
        return Ok(());
    }

    let content = std::fs::read_to_string(&settings_path)
        .map_err(|e| format!("Failed to read settings.json: {}", e))?;
    let mut settings: serde_json::Value = serde_json::from_str(&content)
        .map_err(|e| format!("Failed to parse settings.json: {}", e))?;

    // Restore original statusLine if we saved one
    if let Some(original) = settings.get("_ccrw_original_statusline")
        .and_then(|v| v.as_str())
        .map(|s| s.to_string())
    {
        settings["statusLine"] = serde_json::json!({
            "type": "command",
            "command": original
        });
        if let Some(obj) = settings.as_object_mut() {
            obj.remove("_ccrw_original_statusline");
        }
    } else {
        if let Some(obj) = settings.as_object_mut() {
            obj.remove("statusLine");
        }
    }

    let output = serde_json::to_string_pretty(&settings)
        .map_err(|e| format!("Failed to serialize settings: {}", e))?;
    std::fs::write(&settings_path, output)
        .map_err(|e| format!("Failed to write settings.json: {}", e))?;

    Ok(())
}
