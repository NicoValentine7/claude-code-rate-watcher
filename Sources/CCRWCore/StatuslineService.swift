import Foundation

public struct StatuslineService {
    public static let rateDataFilename = "ccrw-rate-data.json"
    public static let helperScriptFilename = "ccrw-statusline.sh"
    public static let staleThreshold: TimeInterval = 600

    private let fileManager: FileManager
    private let homeDirectory: URL

    public init(
        fileManager: FileManager = .default,
        homeDirectory: URL = FileManager.default.homeDirectoryForCurrentUser
    ) {
        self.fileManager = fileManager
        self.homeDirectory = homeDirectory
    }

    public func rateDataURL() -> URL {
        homeDirectory
            .appendingPathComponent(".claude", isDirectory: true)
            .appendingPathComponent(Self.rateDataFilename)
    }

    public func helperScriptURL() -> URL {
        homeDirectory
            .appendingPathComponent(".claude", isDirectory: true)
            .appendingPathComponent(Self.helperScriptFilename)
    }

    public func readRateData(now: Date = Date()) -> RateLimitSnapshot? {
        let url = rateDataURL()
        guard let data = try? Data(contentsOf: url) else {
            return nil
        }

        let decoder = JSONDecoder()
        guard let payload = try? decoder.decode(StatuslineRateData.self, from: data) else {
            return nil
        }

        if let timestamp = payload.timestamp {
            let age = now.timeIntervalSince(Date(timeIntervalSince1970: TimeInterval(timestamp)))
            guard age <= Self.staleThreshold else {
                return nil
            }
        } else {
            guard isFileFresh(url: url, now: now) else {
                return nil
            }
        }

        return RateLimitSnapshot(
            fiveHourPercent: payload.fiveHourPercent,
            sevenDayPercent: payload.sevenDayPercent,
            fiveHourResetsAt: parseResetDate(payload.fiveHourResetsAt),
            sevenDayResetsAt: parseResetDate(payload.sevenDayResetsAt),
            isLive: true,
            authMissing: false,
            errorMessage: nil,
            errorDetail: nil,
            retryCount: 0,
            retryAt: nil,
            suggestRelogin: false,
            source: .statusline
        )
    }

    public func isInstalled() -> Bool {
        let settingsURL = homeDirectory
            .appendingPathComponent(".claude", isDirectory: true)
            .appendingPathComponent("settings.json")
        let scriptURL = helperScriptURL()
        guard fileManager.fileExists(atPath: scriptURL.path),
              let data = try? Data(contentsOf: settingsURL),
              let json = try? JSONSerialization.jsonObject(with: data) as? [String: Any],
              let statusLine = json["statusLine"] as? [String: Any],
              let command = statusLine["command"] as? String
        else {
            return false
        }

        return command.contains(Self.helperScriptFilename)
    }

    @discardableResult
    public func install() throws -> Bool {
        let scriptURL = helperScriptURL()
        let rateDataURL = rateDataURL()
        let claudeDirectory = homeDirectory.appendingPathComponent(".claude", isDirectory: true)

        try fileManager.createDirectory(at: claudeDirectory, withIntermediateDirectories: true)

        let script = """
        #!/bin/bash
        # CCRW StatusLine Helper — auto-generated by Claude Code Rate Watcher
        # Reads Claude Code statusline JSON from stdin, extracts rate_limits,
        # and writes them to \(rateDataURL.path) for CCRW to consume.

        input=$(cat)
        echo "$input" | python3 -c "
        import sys, json, time
        try:
            data = json.load(sys.stdin)
            rl = data.get('rate_limits', {})
            if not rl:
                sys.exit(0)
            out = {}
            fh = rl.get('five_hour')
            if fh:
                out['five_hour_percent'] = int(fh.get('used_percentage', 0))
                ra = fh.get('resets_at')
                if ra is not None:
                    out['five_hour_resets_at'] = str(ra)
            sd = rl.get('seven_day')
            if sd:
                out['seven_day_percent'] = int(sd.get('used_percentage', 0))
                ra = sd.get('resets_at')
                if ra is not None:
                    out['seven_day_resets_at'] = str(ra)
            out['timestamp'] = int(time.time())
            with open('\(rateDataURL.path)', 'w') as f:
                json.dump(out, f)
        except Exception:
            pass
        " 2>/dev/null
        """

        let alreadyInstalled = isInstalled()
        try script.write(to: scriptURL, atomically: true, encoding: .utf8)
        try fileManager.setAttributes([.posixPermissions: 0o755], ofItemAtPath: scriptURL.path)
        try configureSettings(scriptURL: scriptURL)
        return !alreadyInstalled
    }

    private func configureSettings(scriptURL: URL) throws {
        let settingsURL = homeDirectory
            .appendingPathComponent(".claude", isDirectory: true)
            .appendingPathComponent("settings.json")

        var settings: [String: Any] = [:]
        if let data = try? Data(contentsOf: settingsURL),
           let parsed = try? JSONSerialization.jsonObject(with: data) as? [String: Any] {
            settings = parsed
        }

        if let existing = settings["statusLine"] as? [String: Any],
           let command = existing["command"] as? String,
           !command.contains(Self.helperScriptFilename) {
            settings["_ccrw_original_statusline"] = command
        }

        settings["statusLine"] = [
            "type": "command",
            "command": scriptURL.path,
        ]

        let data = try JSONSerialization.data(withJSONObject: settings, options: [.prettyPrinted, .sortedKeys])
        try data.write(to: settingsURL)
    }

    private func isFileFresh(url: URL, now: Date) -> Bool {
        guard let attributes = try? fileManager.attributesOfItem(atPath: url.path),
              let modified = attributes[.modificationDate] as? Date
        else {
            return false
        }
        return now.timeIntervalSince(modified) < Self.staleThreshold
    }

    private func parseResetDate(_ raw: String?) -> Date? {
        guard let raw else {
            return nil
        }
        if let epochSeconds = TimeInterval(raw) {
            return Date(timeIntervalSince1970: epochSeconds)
        }
        let isoWithFractional = ISO8601DateFormatter()
        isoWithFractional.formatOptions = [.withInternetDateTime, .withFractionalSeconds]
        if let withFractional = isoWithFractional.date(from: raw) {
            return withFractional
        }
        let iso = ISO8601DateFormatter()
        iso.formatOptions = [.withInternetDateTime]
        return iso.date(from: raw)
    }
}

private struct StatuslineRateData: Codable {
    let fiveHourPercent: Int?
    let sevenDayPercent: Int?
    let fiveHourResetsAt: String?
    let sevenDayResetsAt: String?
    let timestamp: UInt64?

    private enum CodingKeys: String, CodingKey {
        case fiveHourPercent = "five_hour_percent"
        case sevenDayPercent = "seven_day_percent"
        case fiveHourResetsAt = "five_hour_resets_at"
        case sevenDayResetsAt = "seven_day_resets_at"
        case timestamp
    }
}
